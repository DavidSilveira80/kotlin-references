package operators

fun main(){
    // Todos os operadores relacionais retornam um booleano true false

    // comparação igual
    println(10 == 10) // true

    // Diferente
    println(10 != 7) // true

    // maior que
    println(5 > 2) // true

    // menor que
    println(5 < 2) // false

    // maior ou igual
    println(5 >= 5) // true

    // menor ou igual
    println(5 <= 5) // true
}